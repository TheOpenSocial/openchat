function buildCountryOptions() {
  try {
    if (typeof Intl.DisplayNames !== "function") {
      return [...FALLBACK_COUNTRY_OPTIONS];
    }
    const displayNames = new Intl.DisplayNames(["en"], { type: "region" });
    const labels = ISO_REGION_CODES.map((code) => displayNames.of(code))
      .filter((label): label is string => Boolean(label))
      .sort((left, right) => left.localeCompare(right, "en"));

    return Array.from(new Set(labels));
  } catch {
    return [...FALLBACK_COUNTRY_OPTIONS];
  }
}

export const COUNTRY_OPTIONS = buildCountryOptions();

export function guessCountryFromLocale() {
  try {
    const locale = Intl.DateTimeFormat().resolvedOptions().locale;
    const regionMatch = locale.match(/[-_](\w{2})$/);
    const region = regionMatch?.[1]?.toUpperCase();
    if (!region) {
      return null;
    }

    if (typeof Intl.DisplayNames === "function") {
      const displayNames = new Intl.DisplayNames(["en"], { type: "region" });
      const label = displayNames.of(region);
      if (label && COUNTRY_OPTIONS.includes(label)) {
        return label;
      }
    }

    return FALLBACK_REGION_TO_COUNTRY[region] ?? null;
  } catch {
    return null;
  }
}
